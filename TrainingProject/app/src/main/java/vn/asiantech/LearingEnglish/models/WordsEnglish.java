public class WordsEnglish {
    private String newWord;
    private String spellingWord;
    private String listenWord;

    public String getNewWord() {
        return newWord;
    }

    public void setNewWord(String newWord) {
        this.newWord = newWord;
    }

    public String getSpellingWord() {
        return spellingWord;
    }

    public void setSpellingWord(String spellingWord) {
        this.spellingWord = spellingWord;
    }

    public String getListenWord() {
        return listenWord;
    }

    public void setListenWord(String listenWord) {
        this.listenWord = listenWord;
    }
}
